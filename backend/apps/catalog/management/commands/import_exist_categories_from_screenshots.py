EXCLUDED_CATEGORY_NAMES = {
    "допомогазсу",
    "технічнадопомогавдорозі",
    "технічнадопомоганадорозі",
}


class Command(BaseCommand):
    help = "Imports EXISTS categories extracted from screenshots archive into catalog.Category."

    @transaction.atomic
    def handle(self, *args, **options):
        created = 0
        reactivated = 0
        reused = 0
        skipped_excluded = 0
        reserved_slugs: set[str] = set()

        for path in SCREENSHOT_CATEGORY_PATHS:
            parent: Category | None = None
            skip_path = False

            for raw_name in path:
                name = sanitize_category_name(raw_name)
                normalized_name = normalized_category_name(name)

                if normalized_name in EXCLUDED_CATEGORY_NAMES:
                    skipped_excluded += 1
                    skip_path = True
                    break

                existing = find_category_by_normalized_name(name=name, parent=parent)
                name_uk, name_ru, name_en = build_category_i18n_names(name)
                if existing is not None:
                    changed_fields: set[str] = set()
                    if not existing.is_active:
                        existing.is_active = True
                        changed_fields.add("is_active")
                    if not existing.name_uk:
                        existing.name_uk = name_uk
                        changed_fields.add("name_uk")
                    if not existing.name_ru or existing.name_ru in {existing.name, existing.name_uk}:
                        existing.name_ru = name_ru
                        changed_fields.add("name_ru")
                    if not existing.name_en or existing.name_en in {existing.name, existing.name_uk}:
                        existing.name_en = name_en
                        changed_fields.add("name_en")
                    if changed_fields:
                        existing.save(update_fields=changed_fields | {"updated_at"})
                        reactivated += 1
                    else:
                        reused += 1
                    parent = existing
                    continue

                category = Category.objects.create(
                    parent=parent,
                    name=name,
                    name_uk=name_uk,
                    name_ru=name_ru,
                    name_en=name_en,
                    slug=generate_unique_category_slug(
                        name=name,
                        reserved_slugs=reserved_slugs,
                    ),
                    is_active=True,
                )
                created += 1
                parent = category

            if skip_path:
                continue

        self.stdout.write(
            self.style.SUCCESS(
                (
                    "Import finished. "
                    f"Created: {created}, reactivated: {reactivated}, reused: {reused}, "
                    f"excluded/skipped: {skipped_excluded}."
                ),
            ),
        )
