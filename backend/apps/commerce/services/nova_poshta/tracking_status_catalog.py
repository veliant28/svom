def resolve_tracking_status_text(*, status_code: str | None, status_text: str | None) -> str:
    normalized_text = str(status_text or "").strip()
    if normalized_text:
        return normalized_text

    normalized_code = str(status_code or "").strip()
    if not normalized_code:
        return ""

    return NOVA_POSHTA_TRACKING_STATUS_BY_CODE.get(normalized_code, "")
